package 복습;

public class 기본형심화 {

	public static void main(String[] args) {
		// 기본형 4가지
		// 정수, 실수, 문자1, 논리
		// int, double, char, boolean
		double temp = 1.2;// 세미콜론 (끝!)
		int floor = 2;
		char color = 'r';
		boolean coffee = false;
		// 실 부품
		String location = "선릉역";

		// sysout 컨트롤 스페이스바
		// 자주사용하는 부품은 대문자로 바로 씁니다.
		// System-컴퓨터, out(출력장치)=모니터
		// in(입력장치)=키보드
		System.out.println("오늘 온도는 " + temp + "도");
		System.out.println("우리집은 " + floor + "층");
		System.out.println("옷 색깔은 " + color);
		System.out.println("커피를 마셨나 " + coffee);
		System.out.println("내 위치는 " + location);

	}

}
