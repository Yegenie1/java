package 반복문;

public class For3 {

	public static void main(String[] args) {

		// 1
		for (int i = 0; i < 10; i++) {
			System.out.printf("*");

		}
		System.out.println("");
		// 2
		for (int i = 0; i < 5; i++) {
			System.out.printf("커피*");
		}
		// 3
		System.out.println("");
		for (int i = 0; i < 3; i++) {
			System.out.println("커피*우유");
		}
		// 4
		for (int i = 0; i < 3; i++) {
			System.out.println(i + 1 + ": 짱!!");
		}
	}

}
